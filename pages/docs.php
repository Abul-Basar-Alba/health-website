        <h2>What is a Diet?</h2>
        <p>
            A diet refers to the foods and beverages a person consumes daily. It includes a combination of macronutrients 
            (carbohydrates, proteins, and fats) and micronutrients (vitamins and minerals) that are essential for 
            maintaining energy levels, supporting bodily functions, and promoting overall health.
        </p>
    </section>
    <section>
        <h2>What is a Healthy Diet?</h2>
        <p>
            A healthy diet is one that includes a variety of nutrient-dense foods from all food groups. It emphasizes 
            fruits, vegetables, whole grains, lean proteins, and healthy fats while limiting processed foods, sugary 
            drinks, and excessive salt. A healthy diet provides the right balance of nutrients to support optimal health 
            and reduce the risk of chronic diseases.
        </p>
    </section>
    <section>
        <h2>What are the Key Components of a Healthy Diet?</h2>
        <p>
            The key components of a healthy diet include:
            <ul>
                <li>Fruits and vegetables: Rich in vitamins, minerals, and antioxidants.</li>
                <li>Whole grains: Provide fiber, B vitamins, and essential minerals.</li>
                <li>Lean proteins: Such as poultry, fish, beans, and nuts.</li>
                <li>Healthy fats: Found in avocados, olive oil, and fatty fish.</li>
                <li>Water: Essential for hydration and overall health.</li>
            </ul>
        </p>
    </section>
    <section>
        <h2>What are the Different Types of Diets?</h2>
        <p>
            There are various types of diets that people follow for health, ethical, or personal reasons. Some common 
            types of diets include:
            <ul>
                <li>Mediterranean diet: Rich in fruits, vegetables, whole grains, and olive oil.</li>
                <li>Ketogenic diet: High in fats, moderate in proteins, and low in carbohydrates.</li>
                <li>Vegan diet: Excludes all animal products.</li>
                <li>Paleo diet: Focuses on foods that our ancestors ate, such as lean meats, nuts, and seeds.</li>
                <li>Gluten-free diet: Eliminates gluten-containing foods for individuals with celiac disease or gluten sensitivity.</li>
            </ul>
        </p>
    </section>
    <section>
        <h2>How to Create a Balanced Diet?</h2>
        <p>
            To create a balanced diet, follow these guidelines:
            <ul>
                <li>Fill half your plate with fruits and vegetables.</li>
                <li>Include whole grains, lean proteins, and healthy fats in your meals.</li>
                <li>Limit sugary, fried, and processed foods.</li>
                <li>Stay hydrated by drinking water throughout the day.</li>
                <li>Practice portion control and mindful eating.</li>
            </ul>
        </p>
